package egovframework.giftednote.user.srce.model;

public class UserSrceVo {
	String N_IDX= "";
	String IDX= "";
	String NAME = "";
	String SCORE = "";
	String S_IDX = "";
	public String getN_IDX() {
		return N_IDX;
	}
	public void setN_IDX(String n_IDX) {
		N_IDX = n_IDX;
	}
	public String getIDX() {
		return IDX;
	}
	public void setIDX(String iDX) {
		IDX = iDX;
	}
	public String getNAME() {
		return NAME;
	}
	public void setNAME(String nAME) {
		NAME = nAME;
	}
	public String getSCORE() {
		return SCORE;
	}
	public void setSCORE(String sCORE) {
		SCORE = sCORE;
	}
	public String getS_IDX() {
		return S_IDX;
	}
	public void setS_IDX(String s_IDX) {
		S_IDX = s_IDX;
	}
	
	
}
